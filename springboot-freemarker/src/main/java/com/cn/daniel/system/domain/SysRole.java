package com.cn.daniel.system.domain;

/**
 * 一个简单角色类，留作以后扩展.
 * @author Daniel
 * @time 2017-07-10
 */

public class SysRole {

	private Integer id;
	private String name;

	public Integer getId() {
		return id;
	}

	public void setId(Integer id) {
		this.id = id;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}
}
